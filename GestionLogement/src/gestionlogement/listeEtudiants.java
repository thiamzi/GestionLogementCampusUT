    public void affichage() {
        try {
            String requete = "SELECT * FROM etudiant, chambre WHERE ID_CHAMBRE!='0' AND ID_CHAMBRE=ID_CH";
            stat = c.ObtenirConnexion().createStatement();
            rest = stat.executeQuery(requete);
            DefaultTableModel tab = (DefaultTableModel) listecod.getModel();
            while (rest.next()) {
                tab.addRow(new String[]{"" + rest.getString("NOM"), "" + rest.getString("PRENOM"),
                    "" + rest.getString("LIBELLE"), "" + rest.getString("DATE"), "" + rest.getString("FILIERE"), "" + rest.getString("NIVEAU")});
                listecod.updateUI();
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

