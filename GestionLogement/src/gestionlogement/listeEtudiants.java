    private void chercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chercherActionPerformed
        String INE = ine.getText();
        result.setText("");
        String requete = "SELECT * FROM etudiant, chambre WHERE INE='" + INE + "' AND ID_CHAMBRE!='0'";
        try {
            stat = c.ObtenirConnexion().createStatement();
            rest = stat.executeQuery(requete);
            DefaultTableModel tab = (DefaultTableModel) search.getModel();
            if (rest.next()) {
                if (tab.getRowCount() == 1) {
                    tab.removeRow(0);
                    tab.addRow(new String[]{"" + rest.getString("NOM"), "" + rest.getString("PRENOM"),
                        "" + rest.getString("LIBELLE"), "" + rest.getString("DATE"), "" + rest.getString("FILIERE"), "" + rest.getString("NIVEAU")});
                    search.updateUI();
                }
                if (tab.getRowCount() == 0) {
                    tab.addRow(new String[]{"" + rest.getString("NOM"), "" + rest.getString("PRENOM"),
                        "" + rest.getString("LIBELLE"), "" + rest.getString("DATE"), "" + rest.getString("FILIERE"), "" + rest.getString("NIVEAU")});
                    search.updateUI();
                }

            } else {
                   if (tab.getRowCount() == 1) {
                       result.setText("Aucun reslutat trouve...");
                       tab.removeRow(0);
                   } 
                    if (tab.getRowCount() == 0) {
                       result.setText("Aucun reslutat trouve...");
                   } 
                
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }

    }//GEN-LAST:event_chercherActionPerformed

